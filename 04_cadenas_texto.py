# Funciones para string siguiendo el cuarto ejercicio del libro
# busquedas de derecha a izquiera y de izquierda a derecha
cadena = input("Introduzca una cadena:")
buscar = input("Introduzca una cadena a buscar:")

print("La cadena aparece en la posicion (find):", cadena.find(buscar))
print("La cadena aparece en la posicion (rfind):", cadena.rfind(buscar))
